-- | Efficient only when partially applied to data array
--   and then evaluated at multiple x-points 
lfCubicSplineEval :: UArr.Vector Double -> Double -> Double
lfCubicSplineEval ys
  | n > 1      = lcse
  | n < 1      = const 0
  | otherwise  = \x
     -> if x<0 || x>1 then 0
                      else ys!0 * ((2*x-1)^2 - 1)^2
 where lcse x | i < 0      = 0
              | i < 1      = Œæ^2 * (3*y‚ÇÅ - √∞‚ÇÅ + Œæ*(√∞‚ÇÅ - 2*y‚ÇÅ))
              | i < n      = y‚ÇÄ
                           + Œæ * (√∞‚ÇÄ
                                  + Œæ * (3*(y‚ÇÅ - y‚ÇÄ) - 2*√∞‚ÇÄ - √∞‚ÇÅ
                                         + Œæ * (√∞‚ÇÅ + √∞‚ÇÄ + 2*(y‚ÇÄ - y‚ÇÅ))))
              | i == n     = (1-Œæ)^2 * (3*y‚ÇÄ + √∞‚ÇÄ + (1-Œæ)*(-√∞‚ÇÄ - 2*y‚ÇÄ))
              | otherwise  = 0
        where hx = x / Œ¥x
              i = floor hx
              Œæ = hx - fromIntegral i
              y‚ÇÄ = ys!(i-1)
              y‚ÇÅ = ys!i
              √∞‚ÇÄ = 0.5 * Œ¥ys!(i-1)
              √∞‚ÇÅ = 0.5 * Œ¥ys!i
       Œ¥ys = UArr.imap (\i _
                  -> if i<1
                      then ys!1
                      else if i<n-1
                            then ys!(i+1) - ys!(i-1)
                            else -ys!(n-2)
              ) ys
       n = Arr.length ys
       Œ¥x = 1 / fromIntegral (n+1)

cubicResample :: Arr.Vector v Double => Int -> v Double -> v Double
cubicResample n ys = Arr.generate n $ \i -> spline $ fromIntegral (i+1) / fromIntegral (n+1)
 where spline = lfCubicSplineEval $ Arr.convert ys

fourierTrafo :: UArr.Vector (Complex Double) -> HomogenSampled Double
fourierTrafo = prepareTrafos 2 FFT.dft process
 where process n' fft = homogenSampled (0.25, 0.75)
                      . Arr.zipWith (\Œº -> realPart . (Œº*)) Œºs
                      . FFT.execute fft
                      . \Œ±s -> Arr.zipWith (*) Œ∑s Œ±s <> Arr.replicate (n' - Arr.length Œ±s) 0
        where Œºs = Arr.generate n' $ \j -> let t = (1-n)/n + 2*fromIntegral j/n
                                           in cis $ -pi * t/2
              Œ∑s = Arr.generate n' $ \k -> cis (-pi*fromIntegral k*(1-n)/n)
              n = fromIntegral n'

invFourierTrafo :: HomogenSampled Double -> UArr.Vector (Complex Double)
invFourierTrafo = prepareTrafos 2 FFT.idft process
 where process n' ift = Arr.zipWith (*) Œ∑s
                      . FFT.execute ift
                      . (\(HomogenSampled (0,1) vs _)
                        -> Arr.zipWith (\(rŒº:+iŒº) z -> rŒº*z :+ iŒº*z) Œºs
                            $ Arr.slice 1 n' vs )
                      . resampleHomogen (-0.5, 1.5) n'
        where Œºs = Arr.generate n' $ \j -> let t = (1-n)/n + 2*fromIntegral j/n
                                           in cis $ pi * t/2
              Œ∑s = Arr.generate (n'`div`2) $ \k -> 2 * cis (pi*fromIntegral k*(1-n)/n)
              n = fromIntegral n'

prepareTrafos :: (UArr.Storable a, UArr.Storable b, DynamicDimension c)
                    => Int -> FFT.Transform a b
                     -> (Int -> FFT.Plan a b -> c -> r)
                     -> c -> r
prepareTrafos sizeFactor transform env = \Œ±s
  -> let lookupTrafo nmin = case Map.lookupGE nmin plans of
          Just (_,f) -> f Œ±s
          Nothing -> lookupTrafo $ (nmin*2)`div`3
     in lookupTrafo $ dynDimension Œ±s * sizeFactor
 where plans = Map.fromList [ (n, env n $ FFT.plan transform n)
                            | p <- [3..10]
                            , œÖ <- [1,0]
                            , let n = 2^p - œÖ*2^(p-2) -- [6,8,12,16,24,32,48.. 1024]
                            ]



